public class DecisionMaking2 {
    
    public static void main(String[] args) {
        
        int num = 30;

        switch (num) {
            case 5:
                System.out.println("num is 5");
                break;
            
            case 20:
                System.out.println("num is 20");
                break;
        
            default:
                System.out.println("Not present.");
        }
    }
}
